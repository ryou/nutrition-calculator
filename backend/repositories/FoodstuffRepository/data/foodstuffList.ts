import { Foodstuff } from '../../../types/foodstuff'

type PFC = 'protein' | 'fat' | 'carbohydrate'

type Vitamin =
  | 'vitaminA'
  | 'vitaminB1'
  | 'vitaminB2'
  | 'vitaminB6'
  | 'vitaminB12'
  | 'vitaminC'
  | 'vitaminD'
  | 'vitaminE'
  | 'vitaminK'

type Mineral =
  | 'potassium'
  | 'calcium'
  | 'magnesium'
  | 'phosphorous'
  | 'iron'
  | 'zinc'
  | 'copper'
  | 'manganese'

type Other = 'salt'

type FoodstuffInput = {
  id: string
  name: string
  subNames: string[]
  unit: {
    text?: string
    amount: number
  }
  nutrition: {
    calorie: number
    pfc: Record<PFC, number>
    vitamin: Record<Vitamin, number>
    mineral: Record<Mineral, number>
    other: Record<Other, number>
  }
}

const parse = (foodstuffInputs: FoodstuffInput[]): Foodstuff[] => {
  return foodstuffInputs.map((foodstuff) => {
    const { nutrition } = foodstuff
    return {
      ...foodstuff,
      nutrition: {
        // TODO: せっかくg単位でデータを保持しているので、クライアント側でもデータ自体はg単位で保持して表示時に変換するようにしたい
        energy_kcal: nutrition.calorie * 0.001,
        protein: nutrition.pfc.protein,
        fat: nutrition.pfc.fat,
        carbohydrate: nutrition.pfc.carbohydrate,
        vitamin_a: nutrition.vitamin.vitaminA * 1000 * 1000,
        vitamin_d: nutrition.vitamin.vitaminD * 1000 * 1000,
        vitamin_e: nutrition.vitamin.vitaminE * 1000,
        vitamin_k: nutrition.vitamin.vitaminK * 1000 * 1000,
        vitamin_b1: nutrition.vitamin.vitaminB1 * 1000,
        vitamin_b2: nutrition.vitamin.vitaminB2 * 1000,
        vitamin_b6: nutrition.vitamin.vitaminB6 * 1000,
        vitamin_b12: nutrition.vitamin.vitaminB12 * 1000 * 1000,
        vitamin_c: nutrition.vitamin.vitaminC * 1000,
        k: nutrition.mineral.potassium * 1000,
        ca: nutrition.mineral.calcium * 1000,
        mg: nutrition.mineral.magnesium * 1000,
        phosphorus: nutrition.mineral.potassium * 1000,
        iron: nutrition.mineral.iron * 1000,
        zn: nutrition.mineral.zinc * 1000,
        copper: nutrition.mineral.copper * 1000,
        mn: nutrition.mineral.manganese * 1000,
        salt: nutrition.other.salt,
      },
    }
  })
}

